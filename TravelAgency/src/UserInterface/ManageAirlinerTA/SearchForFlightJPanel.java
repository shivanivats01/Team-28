    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
         try{
        Flight f;
        String flightNumber = txtFlightNumber.getText();
        f = airliner.getFlightDirectory().searchFlight(flightNumber);
        if(f!=null){
        SearchResultJPanel vpdjp = new SearchResultJPanel(CardSequenceJPanel, f);
        CardSequenceJPanel.add("SearchResultJPanel", vpdjp);
        CardLayout layout = (CardLayout)CardSequenceJPanel.getLayout();
        layout.next(CardSequenceJPanel);
        }
        else{
            JOptionPane.showMessageDialog(null, "Nothing found", "No result found matching your criteria!!", JOptionPane.WARNING_MESSAGE);
            //return;
        }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Nothing found", "No result found matching your criteria!!", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnSearchActionPerformed

