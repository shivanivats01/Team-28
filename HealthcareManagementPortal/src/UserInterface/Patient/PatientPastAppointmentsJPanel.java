    public void populateTable() {
        // populate all patients in patient directory
        ArrayList<WorkRequest> appointmentList = account.getWorkQueue().getWorkRequestList();
        
        ArrayList<WorkRequest> pastAppointmentList = new ArrayList();
        for(WorkRequest w: appointmentList) {
            if(w.getStatus().equals("complete")) {
                pastAppointmentList.add(w);
            }
        }
        
    
        int rowCount = jTable2.getRowCount();
        DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
        for(int i=rowCount-1;i>=0;i--) {
            model.removeRow(i);
        }
        
        for(WorkRequest r: pastAppointmentList) {
            Object row[] = new Object[6];
            row[0] = r.getSender().getId();
            row[1] = r.getReceiver().getId();
            row[2] = r.getStatus();
            row[3] = r.getRequestDate();
            row[4] = r.getResolveDate();
            row[5] = r.getMessage();
            
            model.addRow(row);
        }
    }

